package pratice01;

public class ExampleIfElse {

    public static void main(String[] args) {

        int x = 15;
        int y = 20;

        if(x > y) {
            System.out.println("x is greater then y");

        }else if(x == y){
            System.out.println("x is equal to y");
        }else{
            System.out.println("x is less then y");

        }

    }
}
